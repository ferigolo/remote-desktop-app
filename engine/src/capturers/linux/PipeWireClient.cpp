#include "PipeWireClient.hpp"
#include <unistd.h>

PipeWireClient::PipeWireClient()
{
  pw_init(nullptr, nullptr);
  std::println("[PipeWireClient] Library initialized");
}

PipeWireClient::~PipeWireClient()
{
  disconnect();
  pw_deinit();
}

bool PipeWireClient::connect(int fd, uint32_t node_id, std::function<void(const VideoFrame &)> callback)
{
  onFrameCallback = callback;

  // Create a thread for PipeWire to process video packages and to not block our main thread
  loop = pw_thread_loop_new("pipewire-capture-loop", nullptr);
  context = pw_context_new(pw_thread_loop_get_loop(loop), nullptr, 0); // Manages memory, modules and plugins from our application side
  if (!(loop && context))
    return false;

  // Starts the PipeWire thread and immediately locks it, so it does not crash while we configure the format
  pw_thread_loop_start(loop);
  pw_thread_loop_lock(loop);

  // Tells context to connect with our File Descriptor. Duplicates it so we do not lose reference in case PipeWire thread closes it
  core = pw_context_connect_fd(context, dup(fd), nullptr, 0);
  if (!core)
  {
    pw_thread_loop_unlock(loop);
    return false;
  }

  core_events.version = PW_VERSION_CORE_EVENTS;
  core_events.error = onCoreError;
  pw_core_add_listener(core, &core_listener, &core_events, this);

  // PipeWire manages media (audio, video, sound...). Here we define we want video management for node_id target
  pw_properties *props = pw_properties_new(
      PW_KEY_MEDIA_TYPE, "Video",
      PW_KEY_MEDIA_CATEGORY, "Capture",
      PW_KEY_MEDIA_ROLE, "Screen",
      nullptr);
  // Creates the communication channel
  stream = pw_stream_new(core, "RemoteDesktop Capture Stream", props);
  stream_events.version = PW_VERSION_STREAM_EVENTS;

  // Listeners
  stream_events.state_changed = onStreamStateChanged;
  stream_events.process = onStreamProcess; // Registers the event triggered on each frame generated by the GPU
  pw_stream_add_listener(stream, &stream_listener, &stream_events, this);

  uint8_t buffer[1024];
  // SPA -> Simple Plugin API, uses a dada package system called POD
  // POD -> Plain Old Data
  auto b = SPA_POD_BUILDER_INIT(buffer, sizeof(buffer));

  // Negotiation package
  // Tells PipeWire we want raw video, preferably at BGRx
  struct spa_video_info_raw raw_info = {};
  raw_info.format = SPA_VIDEO_FORMAT_BGRx;

  const spa_pod *param =
      (const spa_pod *)spa_format_video_raw_build(&b,
                                                  SPA_PARAM_EnumFormat,
                                                  &raw_info); // Tells PipeWire to convert the screen into BGRx format

  pw_stream_connect(stream,
                    PW_DIRECTION_INPUT, // We want to receive data, not to emit
                    node_id,
                    static_cast<pw_stream_flags>(PW_STREAM_FLAG_AUTOCONNECT | PW_STREAM_FLAG_MAP_BUFFERS),
                    &param, 1);
  // PW_STREAM_FLAG_MAP_BUFFERS -> tells PipeWire to map VRAM to RAM, so we can access frame.data

  pw_thread_loop_unlock(loop);
  std::println("✅ [PipeWire] Stream created, format requested...");
  return true;
}

void PipeWireClient::disconnect()
{
  if (loop)
  {
    pw_thread_loop_stop(loop);
    pw_thread_loop_lock(loop);
  }

  if (stream)
  {
    pw_stream_disconnect(stream);
    pw_stream_destroy(stream);
    stream = nullptr;
  }
  if (core)
  {
    pw_core_disconnect(core);
    core = nullptr;
  }
  if (context)
  {
    pw_context_destroy(context);
    context = nullptr;
  }

  if (loop)
  {
    pw_thread_loop_unlock(loop);
    pw_thread_loop_destroy(loop);
  }

  loop = nullptr;
}

void PipeWireClient::onCoreError(void *data, uint32_t id, int seq, int res, const char *message)
{
  (void)data;
  (void)seq;
  (void)res;
  std::println(stderr, "❌ [PipeWire] Core critial error (id:{}): {}", id, message);
}

void PipeWireClient::onStreamStateChanged(void *data, pw_stream_state old_state, pw_stream_state state, const char *error)
{
  (void)data;
  static const char *state_names[] = {"ERROR", "UNCONNECTED", "CONNECTING", "PAUSED", "STREAMING"};
  std::println("🌊 [PipeWire] Stream state changed: {} -> {}", state_names[old_state], state_names[state]);

  if (state == PW_STREAM_STATE_ERROR)
  {
    PipeWireClient *self = static_cast<PipeWireClient *>(data);
    if (self->context != nullptr) {
      std::println(stderr, "❌ [PipeWire] Error at Stream: {}", error ? error : "Unknown");
    }
  }
}

void PipeWireClient::onStreamParamChanged(void *data, uint32_t id, const spa_pod *param)
{
  (void)data;
  if (param == nullptr)
    std::println(stderr, "❌ [PipeWire] Wayland server rejected the video format");
  return;
  if (id == SPA_PARAM_Format)
    std::println("✅ [PipeWire] Video format accepted by Wayland. Setting up frame sending...");
}

void PipeWireClient::onStreamProcess(void *data)
{
  PipeWireClient *self = static_cast<PipeWireClient *>(data);

  pw_buffer *b = pw_stream_dequeue_buffer(self->stream);
  if (!b)
    return; // No frame yet

  spa_buffer *buf = b->buffer;
  if (buf->datas[0].data != nullptr)
  {
    VideoFrame frame;
    frame.data = static_cast<uint8_t *>(buf->datas[0].data);
    frame.stride = buf->datas[0].chunk->stride;
    if (self->onFrameCallback)
      self->onFrameCallback(frame);
  }

  pw_stream_queue_buffer(self->stream, b); // Gives the bugger back so PipeWire can write next frame (zero-copy)
}
